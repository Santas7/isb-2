#include <iostream>
#include <ctime>
#define MAX_BIT 128


int main() {
    // инициализируем генератор случайных чисел текущим временем
    srand(time(0));

    // генерируем последовательность в бинарном виде
    for (int i = 0; i < MAX_BIT; i++) {
        unsigned long long rand_num = std::rand() % 32767;
        bool binary_num = rand_num % 2;
        std::cout << binary_num;
    }
    return 0;
}

